"""Prepare the traffic-cone dataset for YOLO fine-tuning.

Source: https://github.com/krisstern/traffic-cone-image-dataset (single class,
263 images, YOLO-format labels, no known copyright). It ships a train-only
split whose yaml points ``train`` and ``val`` at the *same* folder, which would
report dishonest (train-on-val) metrics. This script instead lays the data out
in the standard Ultralytics structure with a reproducible, seeded 80/20 split:

    DATA/traffic_cone/
      images/{train,val}/*.jpg
      labels/{train,val}/*.txt
      traffic_cone.yaml          <- generated data config (consumed by YOLO)

Run once before training:  uv run python -m objdetect.cli.prepare_cone_dataset
"""

import random
import shutil

from objdetect import config

SRC_DIR = config.DATA_DIR / "_cone_src" / "obj_train_data"
OUT_DIR = config.DATA_DIR / "traffic_cone"
VAL_FRACTION = 0.2
CLASS_NAMES = ["traffic cone"]


def main() -> int:
    src_images = SRC_DIR / "images"
    src_labels = SRC_DIR / "labels"
    if not src_images.is_dir():
        raise SystemExit(
            f"source images not found at {src_images}; clone the dataset first:\n"
            "  git clone --depth 1 "
            "https://github.com/krisstern/traffic-cone-image-dataset.git "
            f"{config.DATA_DIR / '_cone_src'}"
        )

    # Pair each image with its label by filename stem; skip any unlabeled image.
    pairs = []
    for image_path in sorted(src_images.iterdir()):
        if image_path.suffix.lower() not in {".jpg", ".jpeg", ".png"}:
            continue
        label_path = src_labels / f"{image_path.stem}.txt"
        if label_path.is_file():
            pairs.append((image_path, label_path))

    # Seeded shuffle so the split is identical on every machine and every run.
    random.seed(config.SEED)
    random.shuffle(pairs)
    val_count = round(len(pairs) * VAL_FRACTION)
    splits = {"val": pairs[:val_count], "train": pairs[val_count:]}

    for split, items in splits.items():
        (OUT_DIR / "images" / split).mkdir(parents=True, exist_ok=True)
        (OUT_DIR / "labels" / split).mkdir(parents=True, exist_ok=True)
        for image_path, label_path in items:
            shutil.copy2(image_path, OUT_DIR / "images" / split / image_path.name)
            shutil.copy2(label_path, OUT_DIR / "labels" / split / label_path.name)

    names_block = "\n".join(f"  {i}: {n}" for i, n in enumerate(CLASS_NAMES))
    yaml_text = (
        "# Traffic-cone detection dataset (single class, not in COCO's 80).\n"
        "# Generated by cli.prepare_cone_dataset — do not edit by hand.\n"
        f"path: {OUT_DIR}\n"
        "train: images/train\n"
        "val: images/val\n"
        f"nc: {len(CLASS_NAMES)}\n"
        "names:\n"
        f"{names_block}\n"
    )
    yaml_path = OUT_DIR / "traffic_cone.yaml"
    yaml_path.write_text(yaml_text)

    print(f"train images: {len(splits['train'])}  val images: {len(splits['val'])}")
    print(f"data config -> {yaml_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
